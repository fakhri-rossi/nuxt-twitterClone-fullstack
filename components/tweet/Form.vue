<template>
  <div>
    <TweetInput :user="props.user" @on-submit="handleFormSubmit" />
  </div>
</template>

<script setup>
const { postTweet } = useTweets();

const props = defineProps({
  user: {
    type: Object,
    required: true
  }
})

async function handleFormSubmit(data){
  try {
    const response = await postTweet({
      text: data.text,
      mediaFiles: data.mediaFiles
    });
    console.log(response);
    
  } catch (error) {
    console.log(error);
  }
}
</script>

